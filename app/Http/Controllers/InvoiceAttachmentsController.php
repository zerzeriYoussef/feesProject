    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $this->validate($request, [

            'file_name' => 'mimes:pdf,jpeg,png,jpg',
    
            ], [
                'file_name.mimes' => 'صيغة المرفق يجب ان تكون   pdf, jpeg , png , jpg',
            ]);
            $image = $request->file('file_name');// traja3 file kano mawjoud si nn null
            $file_name = $image->getClientOriginalName();// traja3 string example.jpg chouf zada tari9a ta3 $imageName louta
            $invoice_number = $request->invoice_number;

            $attachments = new invoice_attachments(); //ma3 save tesna3 new row 
            $attachments->file_name = $file_name;
            $attachments->invoice_number = $invoice_number;
            $attachments->Created_by = Auth::user()->name;
            $attachments->invoice_id = $request->invoice_id;
            $attachments->save();

            // move pic
            $imageName = $request->file_name->getClientOriginalName();
            $request->file_name->move(public_path('Attachments/' . $invoice_number), $imageName);
            session()->flash('Add', 'تم اضافة الفاتورة بنجاح');
            return back();

    }

